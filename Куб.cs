using System;

namespace GeometryLibrary
{
    /// 
    /// Клас, що представляє геометричний об'єкт Куб.
    /// 
    public class Куб
    {
        /// 
        /// Сторона куба.
        /// 
        public double Сторона { get; }

        /// 
        /// Конструктор для створення куба із заданою стороною.
        /// 
        /// Довжина сторони куба. Повинна бути додатнім числом.
        public Куб(double сторона)
        {
            if (сторона <= 0)
            {
                throw new ArgumentException("Сторона куба повинна бути додатнім числом.", nameof(сторона));
            }
            Сторона = сторона;
        }

        /// 
        /// Обчислює об'єм куба.
        /// 
        /// Об'єм куба (V = сторона^3).
        public double ОбчислитиОбєм()
        {
            return Math.Pow(Сторона, 3);
        }

        /// 
        /// Обчислює площу повної поверхні куба.
        /// 
        /// Площа поверхні куба (A = 6 * сторона^2).
        public double ОбчислитиПлощуПоверхні()
        {
            return 6 * Math.Pow(Сторона, 2);
        }
    }
}
